# ─── Scenario builder ────────────────────────────────────────────────────────

def build_scenarios(count, seed=None):
    if seed is not None:
        random.seed(seed)
    scenarios = []
    for _ in range(count):
        r = random.random()
        if   r < 0.45: stype = "happy_path"
        elif r < 0.55: stype = "tool_failure"
        elif r < 0.63: stype = "guardrail_denial"
        elif r < 0.71: stype = "ambiguity"
        elif r < 0.79: stype = "execution_failure"
        elif r < 0.87: stype = "retry"
        elif r < 0.95: stype = "poisoned_tokens"
        else:           stype = "no_llm_needed"

        if stype == "ambiguity" and AMBIGUOUS_QUERIES:
            q = random.choice(AMBIGUOUS_QUERIES)
        elif stype == "no_llm_needed" and NO_LLM_QUERIES:
            q = random.choice(NO_LLM_QUERIES)
        else:
            q = random.choice(QUERY_BANK) if QUERY_BANK else {"question": "?", "answer": "..."}

        prompt_version = "v1.0" if random.random() < PROMPT_VERSIONS.get("v1.0", 0.70) else "v2.0"

        scenarios.append({
            **q,
            "type": stype,
            "prompt_version": prompt_version,
            "is_poisoned": stype == "poisoned_tokens",
        })
    return scenarios


