# ─── Helpers ─────────────────────────────────────────────────────────────────

@contextmanager
def timer():
    start = time.time()
    yield
    print(f"Execution time: {time.time() - start:.2f}s")


def _span_id(span):
    return format(span.get_span_context().span_id, "016x")


def _set_kind(span, kind):
    span.set_attribute(_KIND, kind)


def _set_io(span, input_val, output_val):
    if input_val is not None:
        if isinstance(input_val, str):
            span.set_attribute(_INPUT, input_val)
            span.set_attribute(_INPUT_MIME, _TEXT)
        else:
            span.set_attribute(_INPUT, json.dumps(input_val))
            span.set_attribute(_INPUT_MIME, _JSON)
    if output_val is not None:
        if isinstance(output_val, str):
            span.set_attribute(_OUTPUT, output_val)
            span.set_attribute(_OUTPUT_MIME, _TEXT)
        else:
            span.set_attribute(_OUTPUT, json.dumps(output_val))
            span.set_attribute(_OUTPUT_MIME, _JSON)


def _set_llm_attrs(span, *, system_msg, user_msg, assistant_msg,
                   prompt_tokens=None, completion_tokens=None,
                   template=None, template_version=None, template_vars=None,
                   is_poisoned=False):
    span.set_attribute(_KIND, "LLM")
    span.set_attribute("llm.model_name", LLM_MODEL)
    span.set_attribute("llm.provider", LLM_PROVIDER)
    span.set_attribute("llm.system", LLM_SYSTEM)

    idx = 0
    if system_msg:
        span.set_attribute(f"llm.input_messages.{idx}.message.role", "system")
        span.set_attribute(f"llm.input_messages.{idx}.message.content", system_msg)
        idx += 1
    span.set_attribute(f"llm.input_messages.{idx}.message.role", "user")
    span.set_attribute(f"llm.input_messages.{idx}.message.content", user_msg)
    span.set_attribute("llm.output_messages.0.message.role", "assistant")
    span.set_attribute("llm.output_messages.0.message.content", assistant_msg)

    if is_poisoned:
        pt = prompt_tokens or random.randint(50000, 80000)
        ct = completion_tokens or random.randint(15000, 25000)
    else:
        pt = prompt_tokens if prompt_tokens is not None else random.randint(150, 600)
        ct = completion_tokens if completion_tokens is not None else random.randint(40, 300)
    span.set_attribute("llm.token_count.prompt", pt)
    span.set_attribute("llm.token_count.completion", ct)
    span.set_attribute("llm.token_count.total", pt + ct)

    if template:
        span.set_attribute("llm.prompt_template.template", template)
    if template_version:
        span.set_attribute("llm.prompt_template.version", template_version)
    if template_vars:
        span.set_attribute("llm.prompt_template.variables", json.dumps(template_vars))


def _set_tool_attrs(span, *, tool_name, input_val, output_val,
                    is_mcp=False, mcp_server=None, mcp_version="1.0"):
    span.set_attribute(_KIND, "TOOL")
