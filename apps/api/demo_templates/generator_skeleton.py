def _need_creds():
    space_id = os.environ.get("ARIZE_SPACE_ID", "")
    api_key = os.environ.get("ARIZE_API_KEY", "")
    if not space_id or not api_key:
        print("Error: ARIZE_SPACE_ID and ARIZE_API_KEY must be set (see .env.example).")
        raise SystemExit(1)
