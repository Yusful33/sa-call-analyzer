    # ── --build-prompts (with optional --upload) ──────────────────────────
    if args.build_prompts:
        space_id = os.environ.get("ARIZE_SPACE_ID", "")
        upload_prompts_via_ax(prompt_name=args.prompt_name, space_id=space_id,
                              model=args.prompt_model, upload=args.upload)
