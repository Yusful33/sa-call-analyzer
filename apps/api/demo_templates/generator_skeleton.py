def _dummy_eval(scenario, pass_rate=0.80):
    will_pass = random.random() < pass_rate
    return {
        "label": "pass" if will_pass else "fail",
        "score": 0 if will_pass else 1,
        "explanation": (
            f"Reviewed scenario '{scenario.get('type')}' with question "
            f"\"{scenario.get('question', '')[:80]}\". "
            + ("The trace followed the expected pattern." if will_pass
               else "The trace deviated from the expected pattern at one or more steps.")
        ),
    }


def generate_evaluations(span_data_list):
    """Default implementation — attach one trace_eval per trace.

    Override or extend to add span-level / session-level evals per architectures.md.
    """
    import pandas as pd

    trace_rows = []
    session_rows = []

    for sd in span_data_list:
        scenario = sd["scenario"]
        root_sid = sd.get("root_span_id", "")
        if not root_sid:
            continue
        ev = _dummy_eval(scenario)
        trace_rows.append({
            "context.span_id": root_sid,
            "trace_eval.TrajectoryAccuracy.label": ev["label"],
            "trace_eval.TrajectoryAccuracy.score": ev["score"],
            "trace_eval.TrajectoryAccuracy.explanation": ev["explanation"],
        })

    session_groups = {}
    for sd in span_data_list:
        sid = sd.get("session_id", "")
        if not sid:
            continue
        session_groups.setdefault(sid, []).append(sd)

    for sid, traces in session_groups.items():
        first_root = traces[0].get("root_span_id", "")
        if not first_root:
            continue
        ev = _dummy_eval({"type": "session", "question": f"{len(traces)} turns"})
        session_rows.append({
            "context.span_id": first_root,
            "session_eval.SessionResolution.label": ev["label"],
            "session_eval.SessionResolution.score": ev["score"],
            "session_eval.SessionResolution.explanation": ev["explanation"],
        })

    span_df = pd.DataFrame()   # populate in architecture-specific impls
    trace_df = pd.DataFrame(trace_rows) if trace_rows else pd.DataFrame()
    session_df = pd.DataFrame(session_rows) if session_rows else pd.DataFrame()
    return span_df, trace_df, session_df


# ─── Batch runner ────────────────────────────────────────────────────────────

def run_batch(tracer, count=500, *, with_evals=False, project_name=None, session_size=(3, 6)):
    scenarios = build_scenarios(count)
    span_data_list = []

    print(f"Generating {count} synthetic traces...")
    dist = Counter(s["type"] for s in scenarios)
    print("  Scenario distribution:")
    for stype, cnt in sorted(dist.items(), key=lambda x: -x[1]):
        print(f"    {stype}: {cnt} ({cnt/count*100:.0f}%)")
    print()

    session_id = None
    traces_in_session = 0
    current_session_size = 0

    with timer():
        for i, scenario in enumerate(scenarios):
            if session_id is None or traces_in_session >= current_session_size:
                session_id = f"session_{random.randint(100000, 999999)}"
                current_session_size = random.randint(*session_size)
                traces_in_session = 0

            result = create_trace(tracer, scenario, session_id=session_id)
            traces_in_session += 1
            span_data_list.append({
                "root_span_id": result.get("root", ""),
                "scenario": scenario,
                "session_id": session_id,
                **{k: v for k, v in result.items() if k not in ("root", "final_answer")},
            })

            if ((i + 1) % 100 == 0) or (i + 1 == count):
                print(f"  Created {i+1}/{count} traces...")

        print("\nFlushing spans...")
        flush_timeout = min(30000 + (count // 100) * 5000, 300000)
        ok = trace.get_tracer_provider().force_flush(timeout_millis=flush_timeout)
        print(f"  {'Flush successful' if ok else 'Flush timeout (some spans may still be in flight)'}.")

    if with_evals and span_data_list:
        print("\nWaiting 10s for spans to be indexed before logging evaluations...")
        time.sleep(10)
        print(f"Generating evaluations for {len(span_data_list)} traces...")
        span_df, trace_df, session_df = generate_evaluations(span_data_list)
        print(f"  {len(span_df)} span evals, {len(trace_df)} trace evals, {len(session_df)} session evals.")

        space_id = os.environ.get("ARIZE_SPACE_ID", "")
        api_key = os.environ.get("ARIZE_API_KEY", "")
        if space_id and api_key and project_name:
            try:
                from arize.pandas.logger import Client
                client = Client(space_id=space_id, api_key=api_key)
                if len(span_df):
                    client.log_evaluations_sync(span_df, project_name)
                    print(f"  Logged {len(span_df)} span evaluations.")
                if len(trace_df):
                    client.log_evaluations_sync(trace_df, project_name)
                    print(f"  Logged {len(trace_df)} trace evaluations.")
                if len(session_df):
                    client.log_evaluations_sync(session_df, project_name)
                    print(f"  Logged {len(session_df)} session evaluations.")
                print(f"  All evaluations logged to project '{project_name}'.")
            except Exception as e:
                print(f"  Error logging evaluations: {e}")


